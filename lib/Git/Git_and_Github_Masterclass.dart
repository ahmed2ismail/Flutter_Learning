6. التعامل مع المشاكل (Merge Conflicts & Stash)
=================================================================================================

⚔️ تضارب الأكواد (Merge Conflicts):
بيحصل لما شخصين يعدلوا في نفس السطر في نفس الملف، وتيجي تعمل Merge. Git مش هيعرف يختار كود مين فيهم.
- الحل: Git هيعدل الملف ويحطلك علامات (<<<< ==== >>>>).
- هتفتح الملف في VS Code، وتختار: 
  (Accept Current) أو (Accept Incoming) أو (Accept Both).
- بعد ما تظبط الكود: 
  git add .
  git commit -m "Resolved merge conflict"
- للهروب من الـ Merge والتراجع: git merge --abort

📦 التخزين المؤقت (Git Stash):
لو شغال في برانش ولسه مخلصتش، وطلبوا منك تروح برانش تاني تصلح حاجة ضرورية. لو نقلت، شغلك هيروح أو هيعمل مشكلة.
- git stash : بيخفي تعديلاتك الحالية ويشيلها في "درج" مؤقت، ويرجعك لآخر Commit سليم.
- git stash list : بيعرض كل الحاجات اللي في الدرج.
- git stash apply : بيرجع آخر تعديلات شيلتها في الدرج للملفات.
- git stash pop : بيرجع التعديلات ويمسحها من الدرج (الأكثر استخداماً).
- git stash drop : بيمسح تعديل من الدرج.
- git stash clear : بيفضي الدرج كله.

