2. أساسيات سطر الأوامر (Command Line Basics)
=================================================================================================
- الـ CLI (Command Line Interface) هو واجهة بنكتب فيها الأوامر بدل ما نستخدم الماوس (GUI).
- cd <Path>: للدخول لمجلد معين (مثال: cd Desktop).
- cd .. : للرجوع خطوة للخلف.
- dir : لعرض الملفات جوه المجلد الحالي (في الويندوز)، أو ls (في الماك/لينكس).
- mkdir <Name>: لإنشاء مجلد جديد.
- echo "Text" > file.txt: لإنشاء ملف نصي وكتابة نص جواه.
- del <file.txt>: لحذف ملف.
- cls : لمسح الشاشة (في الويندوز)، أو clear (في الماك/لينكس).

