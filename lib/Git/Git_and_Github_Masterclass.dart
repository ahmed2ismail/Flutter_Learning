8. التعديلات المتقدمة (Rebase & Cherry-Pick)
=================================================================================================

🏗️ إعادة البناء (Git Rebase):
بديل للـ Merge، بيخلي شكل الـ History خطي ومستقيم ونضيف.
بياخد الـ Commits بتاعتك، يفكها، ويحطها "فوق" أحدث شغل في الـ main.
- git switch development
- git rebase main
⚠️ تحذير: لا تستخدم Rebase أبداً على Public Branch شغال عليه ناس غيرك، استخدمه بس في البرانش المحلي بتاعك.

🍒 القطاف (Cherry-Pick):
لو زميلك عمل 5 Commits في برانش تاني، وإنت عايز Commit واحد بس منهم (مثلاً حل لمشكلة معينة) من غير ما تدمج البرانش كله.
- روح للبرانش بتاعك: git switch main
- اقطف الـ Commit: git cherry-pick <Commit_ID>

