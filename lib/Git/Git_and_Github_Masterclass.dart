13. أفضل الممارسات في الكوميت (Conventional Commits)
=================================================================================================

في الشركات الكبيرة، كتابة رسالة Commit عشوائية زي "fixed a bug" غير مقبولة.
بنستخدم معيار اسمه "Conventional Commits" عشان الكود يكون مفهوم للكل:

- feat: إضافة ميزة جديدة (مثال: feat: add login screen)
- fix: حل مشكلة/Bug (مثال: fix: solve app crash on startup)
- docs: تعديل في ملفات التوثيق (مثال: docs: update README)
- style: تعديلات في شكل الكود مسافات/ألوان بدون تغيير اللوجيك (مثال: style: format login code)
- refactor: إعادة صياغة كود شغال لتحسينه (مثال: refactor: optimize search algorithm)
- test: إضافة أو تعديل اختبارات (مثال: test: add unit tests for user logic)
- chore: مهام صيانة روتينية وتحديث مكتبات (مثال: chore: update flutter version)

