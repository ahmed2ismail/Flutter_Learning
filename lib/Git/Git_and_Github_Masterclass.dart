7. التراجع عن الأخطاء (Undo, Reset, Restore, Reflog)
=================================================================================================

🔄 التراجع عن تعديلات لم يتم عمل Commit لها:
- git restore <file> : بيلغي التعديلات اللي عملتها في ملف (في الـ Working Directory) ويرجعه لآخر Commit.
- git restore --staged <file> : بيخرج الملف من الـ Staging Area (كأنك بتلغي git add).
- git clean -fd : بيمسح أي ملفات أو مجلدات جديدة (Untracked) ملهاش لازمة.

⏮️ التراجع عن Commits تم حفظها (Git Reset):
لو عملت Commit وعايز تلغيه:
- git reset --soft HEAD~1 : بيلغي الـ Commit، بس بيسيب تعديلاتك جاهزة في الـ Staging Area.
- git reset --mixed HEAD~1 : (الافتراضي) بيلغي الـ Commit، وبيخرج التعديلات للـ Working Directory.
- git reset --hard HEAD~1 : (خطر ⚠️) بيلغي الـ Commit وبيمسح التعديلات تماماً!

🩹 تعديل آخر Commit (Amending):
لو نسيت ملف أو عايز تغير رسالة آخر Commit (بدل ما تعمل واحد جديد):
# 1. ضيف الملف: git add <file> او git add .
# 2. ادمجه مع الأخير: git commit --amend -m "New Message او الرسالة الاخيرة"
# 3. الرفع الإجباري (لأنك قمت بتغيير الـ Commit بعد أن تم رفعه)
git push -f origin main

🚑 المنقذ الأعظم (Git Reflog):
لو مسحت Branch بالغلط، أو عملت Hard Reset وعايز ترجع في كلامك.
- git reflog : بيعرض سجل "كل حركاتك" في Git (حتى الحاجات اللي اتمسحت).
- هتاخد الـ ID بتاع اللقطة اللي عايز ترجعلها.
- git reset --hard <ID> : هيرجعك للحظة دي بالظبط.

