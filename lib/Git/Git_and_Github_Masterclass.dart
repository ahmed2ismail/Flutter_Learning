10. العمل الجماعي (Collaboration, Fork, Pull Request)
=================================================================================================

🤝 الطريقة الأولى: التعاون المباشر (Collaborators):
(مناسبة للفرق الصغيرة أو أصحاب المشروع)
- صاحب المشروع بيدخل على: Settings > Collaborators > Add People.
- بيبعت دعوة، والشخص التاني بيقبلها.
- كده الشخص التاني يقدر يعمل Clone ويشتغل ويعمل Push مباشرة للـ Repo.

🍴 الطريقة الثانية: الشوكة وطلب السحب (Fork & Pull Request):
(دي الطريقة الرسمية للـ Open Source، لو معندكش صلاحيات على المشروع).
1. Fork: بتدخل على مشروع أي حد وتضغط Fork. GitHub هياخد نسخة طبق الأصل يحطها في حسابك.
2. Clone: هتحمل النسخة "بتاعتك إنت" على جهازك.
3. Branch & Code: هتعمل برانش وتكتب تعديلاتك وتعمل Push لنسختك.
4. Pull Request (PR): هتروح لصفحة المشروع الأصلي، وتضغط Compare & pull request.
   كأنك بتقول لصاحب المشروع: "أنا عدلت الكود ده، إيه رأيك تدمجه عندك؟"
5. صاحب المشروع هيراجع الكود (Code Review) ولو عجبه هيضغط Merge PR.

