4. دورة العمل الأساسية (The Core Workflow)
=================================================================================================
Git بيقسم المشروع لـ 3 أماكن:
1. Working Directory: الفولدر اللي بتكتب فيه الكود دلوقتي.
2. Staging Area: منطقة التجهيز (زي سلة المشتريات)، بتحط فيها الملفات اللي عايز تحفظها.
3. Repository: المخزن النهائي اللي بتتحفظ فيه اللقطات (Commits).

🔥 الأوامر اليومية:
- git status : أهم أمر! بيقلك إنت واقف فين، وإيه الملفات اللي اتعدلت.
- git add <file> : بينقل ملف معين للـ Staging Area.
- git add . : بينقل كل التعديلات للـ Staging Area.
- git commit -m "Message" : بياخد "صورة" للملفات اللي في الـ Staging Area ويحفظها.
- git log : بيعرض تاريخ الـ Commits (الـ ID، مين عمله، إمتى، والرسالة).
- git log --oneline : بيعرض التاريخ بشكل مختصر جداً.

