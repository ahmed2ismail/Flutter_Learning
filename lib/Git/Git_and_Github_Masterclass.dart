16. حالات شائعة وحلولها السحرية (Troubleshooting Cases)
=================================================================================================

🚨 Case 1: "يا نهار أبيض! عملت Commit في الفرع الـ main بدل الـ development!"
الحل:
1. git reset --soft HEAD~1 (الغي الكوميت ورجع التعديلات للـ Staging).
2. git stash (خبيهم في الدرج).
3. git switch development (روح للفرع الصح).
4. git stash pop (طلع التعديلات).
5. git commit -m "Message" (اعمل كوميت في المكان الصح).

🚨 Case 2: "أنا محبوس في وضع Detached HEAD ومش عارف أعمل إيه!"
بيحصل لما تعمل `git checkout <Commit_ID>`.
الحل:
- لو بس كنت بتتفرج وعايز ترجع: `git switch main`
- لو عملت تعديلات وعايز تحتفظ بيها: `git switch -c new-branch-name` (هيعمل فرع جديد من النقطة دي).

🚨 Case 3: "رفعت ملف بالغلط على GitHub وعايز أمسحه من هناك بس يفضل على جهازي!"
(مثلاً رفعت مجلد .idea أو .vscode بالغلط)
الحل:
1. ضيف اسم الملف/المجلد في ملف الـ `.gitignore`.
2. git rm --cached <file_name> (أو `git rm -r --cached <folder>` للمجلدات).
3. git commit -m "Remove tracked file"
4. git push

🚨 Case 4: "مسحت ملف بالغلط وعايز أرجعه!"
الحل:
- `git restore <file_name>` (هيرجعه لآخر حالة كانت في آخر Commit).

