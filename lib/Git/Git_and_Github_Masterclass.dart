15. تنظيف وتعديل التاريخ (Squashing Commits)
=================================================================================================

إنت كنت شغال في Feature وعملت 10 Commits أساميهم عشوائية (زي: test1, test2, ahmed, done).
لو عملتلهم Merge بالشكل ده، شكل الـ History هيبقى سيء جداً. الحل هو الـ Squashing (عصر الكوميتس).

- الأمر: git rebase -i HEAD~5 (لدمج آخر 5 Commits مثلاً).
هتفتحلك شاشة فيها الكوميتس بتاعتك مكتوب جنبهم كلمة `pick`.
- هنسيب أول واحد `pick`، ونغير الباقيين لكلمة `squash` أو `s`.
- لما تحفظ، Git هيدمجهم كلهم في Commit واحد ويطلب منك تكتب رسالة واحدة نظيفة بتعبر عنهم كلهم.

