14. التتبع المتقدم والبحث عن الأخطاء (Bisect & Blame)
=================================================================================================

🔍 إلقاء اللوم (Git Blame):
عايز تعرف مين اللي كتب سطر الكود اللي عمل المشكلة دي؟
- git blame <file_name> : بيعرض الملف وكل سطر جنبه مين المبرمج اللي كتبه، وإمتى، وفي أي Commit.

🐞 البحث الثنائي عن الأخطاء (Git Bisect):
تخيل التطبيق شغال تمام في الإصدار 1.0 (Good)، بس ظهرت فيه مشكلة في الإصدار 2.0 (Bad)، وبينهم 50 Commit!
بدل ما تدور فيهم واحد واحد، Git بيعمل "بحث ثنائي" عشان يلاقي الـ Commit اللي بوظ الدنيا في ثواني:
1. git bisect start
2. git bisect bad (أنا واقف دلوقتي على النسخة البايظة)
3. git bisect good v1.0 (بقول لـ Git إن الإصدار ده كان سليم)
- Git هينقلك لـ Commit في النص بالظبط، تجرب الكود وتقوله:
  - لو شغال: git bisect good
  - لو بايظ: git bisect bad
وهكذا لحد ما Git يقولك: "ده أول Commit عمل المشكلة!".
4. git bisect reset (للخروج من الوضع بعد إيجاد المشكلة).

