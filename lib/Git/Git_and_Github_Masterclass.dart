1. مقدمة: ما هو Git وما هو GitHub؟
=================================================================================================

💡 ما هو Git؟
- هو نظام للتحكم في الإصدارات (Version Control System).
- بيحل مشكلة "ملف واحد وتعديلات كتير تضيع الشغل القديم" أو "نسخ كتير من نفس الملف final_v1, final_v2".
- بيشتغل على جهازك (Local) وبيقوم بوظيفتين أساسيتين:
  1- Tracking Changes: بيتتبع أي حرف يتكتب أو يتمسح.
  2- Version Control: بيحفظ "لقطات" (Snapshots) للمشروع عشان تقدر ترجع لأي نقطة في الماضي.

💡 ما هو GitHub؟
- هو منصة على الإنترنت (Cloud) بتستضيف (Host) المشاريع اللي بتستخدم Git.
- بيخليك تقدر ترفع كودك، تشاركه مع فريقك، وتعمل Portfolio لمشاريعك.
- Git هو "الأداة"، و GitHub هو "المكان اللي بنرفع عليه الشغل".

