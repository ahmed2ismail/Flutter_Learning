5. الفروع والدمج (Branches & Merging)
=================================================================================================

🌱 ما هي الـ Branches؟
البرانش هو مسار منفصل للكود. بنستخدمه عشان نجرب ميزة جديدة (Feature) أو نحل مشكلة (Bug) من غير ما نبوظ الكود الأساسي (main).

- git branch : لعرض كل الفروع (الفرع اللي جنبه * هو اللي إنت واقف عليه).
- git branch <name> : لإنشاء فرع جديد.
- git switch <name> : للانتقال لفرع تاني (أفضل وأحدث من git checkout).
- git switch -c <name> : لإنشاء فرع والانتقال إليه في خطوة واحدة.
- git branch -d <name> : لحذف فرع (لو تم دمجه).
- git branch -D <name> : لحذف فرع بالإجبار (حتى لو لم يتم دمجه).

🔗 الدمج (Merging):
عشان تدمج كود من فرع (development) لفرع (main):
1- روح للفرع الأساسي: git switch main
2- ادمج الفرع التاني فيه: git merge development

أنواع الدمج:
1. Fast-Forward: لو الـ main متعدلش من ساعة ما عملت development، الـ main هيتحرك للأمام ببساطة.
2. 3-Way Merge (Ort): لو الـ main اتعدل والـ dev اتعدل، Git هيعمل Commit جديد يجمع الاتنين مع بعض.

