11. المنظمات والفرق (Organizations & Teams)
=================================================================================================

لما الشركة تكبر، الحساب الشخصي مش بيكفي. بنحتاج Organization Account.

🏢 مميزات الـ Organization:
- Teams: بدل ما تضيف الأشخاص واحد واحد على كل مشروع، بتعمل "فريق" (مثلاً Mobile-Team) وتضيف المبرمجين جواه، وتدي الفريق ده صلاحيات على مشاريع معينة.
- Roles (الصلاحيات):
  1. Read: يشوف الكود بس.
  2. Write: يكتب ويرفع كود (للمبرمجين).
  3. Admin: تحكم كامل (حذف، إضافة أشخاص).

🛡️ حماية الفروع (Branch Protection Rules):
عشان نمنع أي مبرمج يرفع كود مليان أخطاء على الـ main مباشرة:
- Settings > Branches > Add rule.
- نفعل: Require a pull request before merging (مفيش كود يدخل الـ main إلا عن طريق PR).
- نفعل: Require approvals (لازم مبرمج تاني يراجع الكود ويوافق عليه).

