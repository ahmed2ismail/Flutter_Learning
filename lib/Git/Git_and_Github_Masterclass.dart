9. التعامل مع GitHub (Remote Repositories)
=================================================================================================

الـ Remote Repo هو النسخة السحابية من مشروعك.

🔗 ربط المشروع المحلي بـ GitHub:
1. git remote add origin <URL> (بتربط مشروعك باللينك، وسميناه origin اختصاراً).
2. git branch -M main (بنتأكد إن اسم البرانش main مش master).
3. git push -u origin main (بنرفع الكود، والـ "-u" بتخلي Git يحفظ المسار ده للمرات الجاية).

☁️ التعامل اليومي مع السحابة:
- git push : لرفع تعديلاتك من جهازك لـ GitHub.
- git pull : لسحب أحدث التعديلات من GitHub لجهازك ودمجها فوراً (Pull = Fetch + Merge).
- git fetch : بيجيب معلومات عن التعديلات اللي حصلت على السحابة بس مش بيدمجها في ملفاتك.
- git clone <URL> : لتحميل مشروع كامل من GitHub لجهازك لأول مرة.

⚠️ الرفع الإجباري (Force Push):
لو عملت amend أو reset لـ commit كان مرفوع أصلاً، GitHub هيرفض الـ push العادي.
- git push -f origin main (استخدمه بحذر شديد لأنه بيمسح الـ History القديم من السحابة).

