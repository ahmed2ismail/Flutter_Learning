12. أدوات احترافية (.gitignore, Tags, README, Aliases)
=================================================================================================

🙈 ملف التجاهل (.gitignore):
ملف بنعمله في المشروع بنكتب فيه أسماء الملفات اللي مش عايزين نرفعها لـ GitHub 
(زي فولدر build في فلاتر، أو ملفات .env اللي فيها باسووردات).
- مثال لمحتوى الملف:
  *.log      # تجاهل أي ملف بينتهي بـ .log
  build/     # تجاهل مجلد الـ build باللي فيه

🏷️ الإصدارات (Tags):
لما نخلص نسخة من التطبيق (مثلاً الإصدار الأول)، بنعمل Tag عشان نعلمه.
- git tag v1.0 (إصدار خفيف).
- git tag -a v1.0 -m "First Release" (إصدار معتمد برسالة).
- git push origin --tags (لرفع الـ Tags لـ GitHub).

📝 واجهة المشروع (README.md):
ملف بيشرح مشروعك بيعمل إيه وإزاي نشغله، بيتكتب بلغة Markdown.
# عنوان رئيسي
## عنوان فرعي
**خط عريض**
- عنصر في قائمة
[اسم الرابط](اللينك)

🚀 اختصارات الأوامر (Git Aliases):
عشان تكتب بسرعة بدل الأوامر الطويلة:
- git config --global alias.st status (دلوقتي تقدر تكتب git st بدل status).
- git config --global alias.cm "commit -m"

