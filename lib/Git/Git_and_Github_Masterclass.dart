17. ميزات GitHub الإضافية (Issues, Projects, Actions)
=================================================================================================

🛠️ تتبع المشاكل (GitHub Issues):
- مكان جوه الـ Repo بتسجل فيه الـ Bugs اللي محتاجة تتحل، أو الـ Features اللي ناويين نعملها.
- تقدر تعمل Mention لزميلك، وتحط Labels (زي: Bug, Enhancement).
