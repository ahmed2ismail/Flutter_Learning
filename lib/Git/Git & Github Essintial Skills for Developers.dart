-- Command Line Default Path --
- اول مبفتح ال CMD بكون موجود في المسار الاساسي اللي هو : C:\Users\PC
- dir (directly) : امر عرض كل الملفات والمجلدات اللي موجودة في المسار اللي انا واقف عليه
-- Relative Path --> يعني المسار النسبي : يعني انا بكتب المسار من المكان اللي انا واقف عليه مثلا لو انا واقف في C:\Users\PC وعايز اروح ل Desktop هكتب cd Desktop
-- Absolute Path --> يعني المسار المطلق : يعني انا بكتب المسار كامل من اول ال C:\Users\PC لحد ما اوصل ل Desktop هكتب cd C:\Users\PC\Desktop
-- بعض الاوامر المهمة في ال CMD :
- cd (change directly) امر تغيير المسار لمسار اخر : ثم المسار الجديد مثلا cd Desktop --> دا اسمه relative path او cd C:\Users\PC\Desktop --> دا اسمه absolute path
- cd C:\Users\PC\Desktop : امر الانتقال لمسار معين
- C:\> cd d : امر الانتقال من مسار C:\ ل D:\ 
- cd .. : امر الرجوع للمسار السابق
- cd \ : امر الرجوع للمسار الاساسي
- mkdir (make directory) : امر انشاء مجلد جديد : ثم اسم المجلد مثلا mkdir git-project
- mkdir "git project" : امر انشاء مجلد جديد وفيه مسافة : ثم اسم المجلد بين علامتي تنصيص عشان المسافة ما تعملش مشكلة في الامر
- echo (echo) : امر انشاء ملف جديد بمحتوي او بدون محتوي : ثم اسم الملف مع الامتداد مثلا echo > file.txt ولكن هيحتوي علي Echo is on كمحتوي جواه يعني مش هيفضل فاضي ولكن لو عايز اعمل ملف جديد بدون اي محتوي هكتب echo. > file.txt
- echo. > file.txt : كده هيعمل ملف جديد اسمه file2.txt بس من غير اي محتوي في الملف
- echo "hello world" > file.txt : امر انشاء ملف جديد بمحتوي : ثم اسم الملف مع الامتداد وبعدها المحتوي اللي عايز اكتبه في الملف
- echo Hello Students We Are Learning Git > Welcome.txt : كده هيعملي ملف جديد اسمه Welcome.txt وفيه Hello Students We Are Learning Git
- del (delete) امر حذف ملف معين : ثم اسم الملف اللي عايز احذفه مثلا del Welcome.txt مع العلم ان انا بمسح المسار اللي اسمه Welcome.txt اللي انا واقف عليه يعني لو انا واقف في C:\Users\PC\Desktop وكتبت del Welcome.txt هيحذف الملف اللي اسمه Welcome.txt اللي موجود في ال Desktop بس مش هيحذف اي ملف تاني اسمه Welcome.txt لو كان موجود في مسار تاني
- rmdir (remove directory) : امر حذف مجلد معين : ثم اسم المجلد مثلا rmdir git-project مع العلم ان انا بمسح المسار اللي اسمه git-project اللي انا واقف عليه يعني لو انا واقف في C:\Users\PC\Desktop وكتبت rmdir git-project هيحذف المجلد اللي اسمه git-project اللي موجود في ال Desktop بس مش هيحذف اي مجلد تاني اسمه git-project لو كان موجود في مسار تاني
- copy Welcome.txt folder1 : copy امر نسخ ملف معين لمجلد معين : ثم مسار الملف اللي عايز انسخه وبعد كده مسار المجلد اللي عايز انسخه فيه
- move Welcome.txt folder2 : move امر نقل ملف معين لمجلد معين : ثم مسار الملف اللي عايز انقله وبعد كده مسار المجلد اللي عايز انقله فيه
- copy (copy file) : امر نسخ ملف معين : ثم اسم الملف اللي عايز انسخه وبعد كده اسم الملف الجديد اللي عايز اعمل منه نسخة مثلا copy file.txt file2.txt
- copy con (copy content) : امر انشاء ملف جديد : ثم اسم الملف مثلا copy con file.txt وبعد كتابة الامر دا هيخليني اكتب في الملف اللي انا انشاته وبعد ما اخلص بضغط علي ctrl + z عشان احفظ التعديلات اللي انا عملتها في الملف
