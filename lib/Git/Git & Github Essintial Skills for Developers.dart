_______________________________ How Git Works ? _______________________________
_______________________________________________________________________________

- لما بعمل مشروع جديد واربطه ب ال git ف المشروع دا بيتحول ل working directory وبكدا ال git بتبقي حاطة عينها علي المشروع بتاعي ومتابعاه و git بت track المشروع بتاعي عن طريق انها بتاخد snapshot وبالتالي انا بقدر اشتغل فيه واعمل تعديلات عليه وبكدا انا بقدر اعمل commit للتعديلات اللي انا عملتها في المشروع دا وبكدا انا بقدر ارجع لاي نقطة قديمة كنت فيها عن طريق ال commit اللي انا عملته في اللحظة دي
- لما بعمل commit للتعديلات اللي انا عملتها في المشروع دا المشروع دا بيتحول ل staging area يعني انا بقدر اراجع التعديلات اللي انا عملتها في المشروع دا قبل ما اعمل commit للتعديلات دي في ال Git Repository بتاعي وبكدا انا بقدر اتاكد ان التعديلات اللي انا عملتها في المشروع دا صحيحة ومناسبة للمشروع دا قبل ما اعمل commit للتعديلات دي في ال Git Repository بتاعي

- Git بيستخدم نظام اسمه Snapshot : يعني هي بتاخد صورة او بتفتكر الوضع الحالي وبتفتكر التغيرات اللي بتحصل وبالتالي كل ما اعمل تعديل في الملف بتاعي واعمل commit لملفي دا بيعمل snapshot او لقطة للملف بتاعي في اللحظة دي وبيحفظها في ال Git Repository بتاعي وبكدا انا اقدر ارجع لاي نقطة قديمة كنت فيها عن طريق ال snapshot اللي انا عملته في اللحظة دي
- Git بيستخدم نظام اسمه Branching : يعني انا اقدر اعمل نسخ من الملف بتاعي في نفس الوقت اللي انا شغال فيه يعني لو انا عندي مشروع معين وعايز اعمل تعديل معين في المشروع دا بس مش عايز اعدل في النسخة الاساسية من المشروع دا فانا بقدر اعمل نسخة جديدة من المشروع دا واشتغل فيها وبكدا انا اقدر اعمل تعديلات كتير في النسخة الجديدة دي بدون ما ااثر علي النسخة الاساسية من المشروع دا
- Git بيستخدم نظام اسمه Merging : يعني انا اقدر ادمج التعديلات اللي انا عملتها في النسخة الجديدة دي مع النسخة الاساسية من المشروع دا يعني لو انا عملت تعديل معين في النسخة الجديدة دي وعايز ادمجه مع النسخة الاساسية من المشروع دا فانا بقدر اعمل merge للتعديلات اللي انا عملتها في النسخة الجديدة دي مع النسخة الاساسية من المشروع دا وبكدا التعديلات اللي انا عملتها في النسخة الجديدة دي هتظهر في النسخة الاساسية من المشروع دا
- Git بيستخدم نظام اسمه Remote Repositories : يعني انا اقدر اخزن مشروعي في مكان معين علي الانترنت زي Github مثلا وبكدا انا اقدر اوصل لمشروعي دا من اي مكان في العالم وبكدا ا나 بقدر استغل علي مشروعي دا مع زمايلي في نفس الوقت وبكدا انا بقدر ال التعديلات كتير في مشروعي دا بدون ما تاثر علي النسخة الاساسية من المشروع D
- Git بيستخدم نظام اسمه Tags : يعني انا اقدر اعمل tag لنسخة معينة من مشروعي دا وبكدا انا اقدر ارجع لنسخة معينة من مشروعي دا عن طريق ال tag اللي انا عملته في اللحظة دي

