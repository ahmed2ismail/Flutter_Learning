_______________________________ Git Ignore Best Practices ___________________________

- عشان تعمل ignore لملفاتك بشكل احترافي وتضمن إن الـ Repository بتاعك نضيف وآمن، اتبع القواعد دي:

1- **إنشاء الملف:** لازم يكون اسم الملف `.gitignore` (يبدأ بنقطة) ويكون موجود في الـ Root folder (المجلد الرئيسي للمشروع).

2- **القواعد الشائعة للكتابة:**
   - `file.txt`: يتجاهل ملف محدد باسمه في أي مكان في المشروع.
   - `*.log`: يتجاهل أي ملف ينتهي بامتداد `.log`.
   - `temp/`: يتجاهل مجلد اسمه `temp` وكل محتوياته.
   - `/debug.log`: يتجاهل ملف `debug.log` الموجود في المجلد الرئيسي فقط (مش اللي جوه فولدرات تانية).
   - `*.exe`: يتجاهل كل ملفات التشغيل.

3- **استخدام القوالب الجاهزة (Recommended):**
   - بدل ما تكتب كل حاجة بنفسك وتنسى ملفات مهمة، استخدم موقع [gitignore.io](https://www.toptal.com/developers/gitignore).
   - بتكتب اسم التقنية اللي شغال بيها (مثلاً: `Flutter` أو `Android` أو `Node`) وهو بيطلعلك ملف جاهز فيه كل الحاجات اللي المفروض تعملها ignore للتقنية دي.

4- **تجاهل ملف تم عمل Track له بالفعل:**
   - لو رفعت ملف بالغلط وعايز تعمله ignore دلوقتي، مجرد كتابته في الـ `.gitignore` مش كفاية، لازم تشيله من الـ Cache بتاع Git بالأمر ده:
   - `git rm --cached fileName`
