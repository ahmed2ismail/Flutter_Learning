- git tag v1.0 : بيعمل Lightweight tag (مجرد مؤشر لـ commit معين).
- git tag -a v1.1 -m "Release version 1.1" : بيعمل Annotated tag (بيخزن بيانات الشخص اللي عمل الـ tag والتاريخ ورسالة توضيحية، وده الأفضل للإصدارات الرسمية).
- git show v1.1 : بيعرض تفاصيل الـ tag والـ commit اللي مربوط بيه.
- git push origin v1.1 : لرفع tag معين للـ Remote Repository.
- git push origin --tags : لرفع كل الـ tags اللي عملتها محلياً مرة واحدة لـ GitHub.
- git tag -d v1.1 : لحذف الـ tag من جهازك (Local).
- git push origin --delete v1.1 : لحذف الـ tag من على GitHub (Remote).

_______________________________ Git Alias ___________________________________

- الـ Alias هو اختصار لأوامر Git الطويلة اللي بتستخدمها كتير عشان توفر وقت ومجهود.
- git config --global alias.st status : كده بدل ما تكتب git status، هتكتب بس git st.
- git config --global alias.cm "commit -m" : كده بدل ما تكتب git commit -m "msg"، هتكتب git cm "msg".
- git config --global alias.br branch : اختصار لـ git branch.
- git config --global alias.hist "log --pretty=format:'%h %ad | %s%d [%an]' --graph --date=short" : ده مثال لاختصار معقد بيعرض الـ log بشكل شجري ومنظم جداً بمجرد كتابة git hist.

_______________________________ Git Blame ___________________________________

- git blame fileName : أمر "اللوم"، بيستخدم عشان تعرف مين اللي كتب كل سطر في ملف معين، وإيه الـ commit ID اللي اتعدل فيه السطر ده وتاريخ التعديل. مفيد جداً لما تلاقي bug في سطر وعايز تسأل اللي كتبه كان يقصد إيه.

_______________________________ Git Diff ____________________________________

- git diff : بيعرض الفرق بين التعديلات اللي عملتها في الـ Working Directory وبين آخر نسخة في الـ Staging Area.
- git diff --staged : بيعرض الفرق بين اللي في الـ Staging Area وبين آخر Commit عملته.
- git diff branch1 branch2 : بيقارن بين محتويات ملفات برانشين مختلفين عشان تعرف إيه الفرق بينهم قبل ما تعمل Merge.
- git diff commitID1 commitID2 : بيقارن بين حالتين للمشروع في توقيتين مختلفين عن طريق الـ ID بتاع الـ Commits.

