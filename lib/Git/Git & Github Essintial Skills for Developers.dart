-------------------------- GitHub Actions -----------------------------
_______________________________________________________________________

- الـ **GitHub Actions** هي أداة الـ **CI/CD** (التكامل المستمر والنشره المستمر) الخاصة بـ GitHub.
- فكرتها إنك بتعمل "Automated Workflows" (سلسلة خطوات آلية) بتتنفذ لما يحصل حدث معين في الـ Repo.

### أمثلة على استخدام GitHub Actions:
1. **Run Tests:** أول ما حد يبعت Pull Request، الـ Actions بتشغل الـ Unit Tests تلقائياً وتتأكد إن الكود مش بيكسر المشروع.
2. **Build & Deploy:** أول ما تعمل Push على الـ `main` branch، الـ Actions بتبني النسخة الجديدة (Build) وترفعها على السيرفر أو الـ Store (Deploy).
3. **Code Linting:** التأكد من إن الكود مكتوب بتنسيق صحيح (Formatting) حسب قواعد اللغة.

### مكونات الـ Workflow:
- **Events:** الحاجة اللي بتبدأ الـ Action (زي `push`, `pull_request`).
- **Jobs:** مجموعة من الخطوات اللي بتتنفذ مع بعض.
- **Steps:** الأوامر الفعلية اللي بتتنفذ (زي `run: npm install` أو `run: flutter build apk`).
- **Runners:** السيرفرات اللي الـ GitHub بيوفرها عشان يشغل عليها الكود بتاعك (Ubuntu, Windows, macOS).

-------------------------- GitHub Wiki --------------------------------

- الـ **GitHub Wiki** هو مكان مخصص لكتابة التوثيق (Documentation) الطويل للمشروع بتاعك.
- **الفرق بينه وبين الـ README.md:**
  - الـ **README**: بيكون فيه نظرة عامة سريعة، إزاي تسطب المشروع، ولينكات مهمة.
  - الـ **Wiki**: بيكون فيه شرح مفصل لكل جزء في الكود، الـ API Reference، الـ Architecture بتاع المشروع، ودليل كامل للمستخدمين.
- الـ Wiki بيتحفظ كـ Repository منفصل تماماً تقدر تعمل له Clone وتعدل فيه وترفعه تاني.

