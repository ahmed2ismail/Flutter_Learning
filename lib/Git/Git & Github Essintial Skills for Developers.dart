_______________________________ Git Commands ________________________________
_____________________________________________________________________________

- لو انا في مشروع وشغال معايا تيم عليه وعايزين نعمل commit ونعرف مين اللي عمل التعديلات دي فانا بقدر اعمل commit للتعديلات دي وبكدا ال Git بتسجل اسم الشخص اللي عمل التعديلات دي في ال commit message وبالتالي انا بقدر اعرف مين اللي عمل التعديلات دي في المشروع دا
- git config --global user.name "Your Name" : امر انشاء اسم المستخدم اللي هيظهر في ال commit message لما اعمل commit للتعديلات اللي انا عملتها لاي مشروع مرتبط ب Git Repository في الجهاز بتاعي
- git config --global user.email "Your Email" : امر انشاء البريد الالكتروني اللي هيظهر في ال commit message لما اعمل commit للتعديلات اللي انا عملتها لاي مشروع مرتبط ب Git Repository في الجهاز بتاعي

- git config --list : امر عرض كل الاعدادات اللي انا عملتها في ال Git Repository بتاعي يعني بيعرضلي اسم المستخدم والبريد الالكتروني اللي انا عملتهم في ال Git Repository بتاعي
- git config --global --list : امر عرض كل الاعدادات اللي انا عملتها في ال Git Repository بتاعي يعني بيعرضلي اسم المستخدم والبريد الالكتروني اللي انا عملتهم في ال Git Repository بتاعي مع العلم ان ال global دي بتعرضلي الاعدادات اللي انا عملتها لكل المشاريع المرتبطة ب Git Repository في الجهاز بتاعي اما لو كتبت git config --list بدون ال global دي بتعرضلي الاعدادات اللي انا عملتها في المشروع اللي انا واقف عليه بس

- git init(initialize) : امر انشاء Git Local Repository جديد في المسار اللي انا واقف عليه عشان يحول المشروع ل working directory وبتنشئ ملف مخفي اسمه .get وال get repository عايدة علي الملف دا فقط لاغير وال repo دي بتكون محلية يعني علي الجهاز فقط وبعدين بربطها ب github repository عشان تبقي remote repository
--- Hidden Folder .get --> بيكون عبارة عن حاجتين (Staging Area , Repo Hoistory)
-- ال Staging Area: هي المرحلة الموجودة في النص مابين الامر اللي بنفذه في الاول خالص اللي هو ال git init والامر اللي بنفذه في الاخر خالص اللي هو ال commit اللي بيعمل save ل snapshot اللي هي التغيرات الحالية
- في ال Staging Area انا بحدد انهي files انا عايز اعملها save قبل معملها save لان انا ممكن وانا شغال انسي واعدل في ملفات كتيرة جدا وعملت save علي الوضع دا بعد كل التعديلات دي ودا طبعا بيعملي مشكلة في المستقبل عشان لما احب ارجع مثلا علي feature معينة انا عملتها او علي شكل معين للتطبيق مش هعرف ارجعله لان انا في المرة الواحدة بعمل تغييرات كتيرة جدا وبحفظهم والحفظ دا مش بيعبر عن حاجة واحدة وبالتالي التنظيم بتاعنا بيكون سيئ جدا
- يعني انا بقول انا عايز الملف دا يروح علي ال staging area لان دي بق اللي لما باجي اعمل commit هو بيبص علي الموجود في ال staging area وبيتدي يعمله commit ومش بيعمل commit لكل الملفات اللي اتعدل فيها بل للملفات اللي موجودة فقط في ال staging area
- git ls-files : امر عرض كل ال files اللي موجودة في ال staging area يعني بيعرضلي الملفات اللي انا حددتها عشان اعملها save في ال Git Repository بتاعي

-- ال Repo Hoistory: دي اللي بتحتفظ بالتاريخ بتاع ال commits الموجودة في المكان دا وفيها كل ال commits اللي انا عملتها يعني هي عبارة عن سجل
- git status : امر عرض حالة المشروع دا في ال Git Repository بتاعي يعني بيعرضلي التعديلات اللي انا عملتها في المشروع دا والتعديلات اللي لسه ما عملتش لها commit في ال Git Repository بتاعي
- ال git status بتديني 3 حاجات:
1- on branch branchName --> دا ال branch اللي انا واقف عليه
2- لو مفيش commits هيقولي no commits yet او nothing to commit , working tree clean
3- Untracked files: يعني عندي ملفات git مش بتراقبه
- بيكون عندنا مرحلة ما قبل ال git status وهي ان انا لسه معملتش stage (git add) للملف ووقتها بيظهرلنا بعد استخدام ال git status : Untracked file
- لما بنعمل git status بيكون عندنا 3 مراحل بنتحرك فيهم :
1- Changes Unstaged for commit , Modified: fileName , Changes in File --> بتجيلي لما اكون عامل stage اللي هي git add لملف وبعدين انا عدلت فيه او غيرت حاجة
2- التعديلات بتروح علي ال staging area يعني قريب هعمل للتعديلات دي commit --> Changes to be Committed: وتحتها Modified: fileName لو تم التعديل في الملف او اسم الملف لو متمش التعديل فيه
3- ان التعديلات دي يتعملها commit --> خلاص كده اتعملها commit واتحفظت ك snapshots in repository

- git add fileName : دا الامر اللي بيعمل track لل files وبينقلها ل staging area
- git add . : امر اضافة كل التعديلات اللي انا عملتها في المشروع دا ل staging area عشان اقدر اعمل commit للتعديلات دي في ال Git Repository بتاعي
- git commit -m "Commit Message" : امر عمل commit للتعديلات اللي انا عملتها في المشروع دا في ال Git Repository بتاعي مع كتابة رسالة توضيحية للتعديلات دي في ال commit message
- git log : امر عرض كل ال commits اللي انا عملتها في المشروع دا في ال Git Repository بتاعي وبتجيبلي ال id بتاع ال commits
- git log --oneline : امر عرض كل ال commits اللي انا عملتها في المشروع دا في ال Git Repository بتاعي بشكل مختصر يعني بيعرضلي ال commit message بس بدون تفاصيل تانية
- git log --graph : امر عرض كل ال commits اللي انا عملتها في المشروع دا في ال Git Repository بتاعي بشكل بياني يعني بيعرضلي ال commits اللي انا عملتها في المشروع دا في شكل شجرة بتوضحلي ال branches اللي انا عملتها في المشروع دا وال commits اللي انا عملتها في كل branch
