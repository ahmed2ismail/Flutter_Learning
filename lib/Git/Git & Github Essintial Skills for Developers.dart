============== fast forward merge >>>
- لو انا عندي 2 branch زي :
- main branch فيه commit1, c2, c3 
- development branch: c1, c2, c3, c4, c5 --> عملت تفرع جديد من اول ال commit 3 وزودت حجات تانية
فلو جيت وعملت merge from development branch on main branch عن طريق اكون موجود في ال main branch واستخدم امر git merge development فدا اسمه fast forward merge لان ال merge في الحالة دي ابسط ما يمكن لان انا معنديش اي تعديلات حصلت في ال main branch من ساعة لما انشئت ال developers branch وزودت عليه
فبالتالي ال head في ال main بدل ميشاور علي commit 3 بعد ال merge بق يشاور علي ال commit 5 لان من بعد commit 3 in main branch مفيش اي حاجة اتغيرت فيه فبالتالي كل commits الموجودة في ال development هتروح لل main مباشرة
وكده اي اختلاف عن دا هياخدني علي ال non fast forward merge

============== non fast forward merge >>>
- لو انا عندي 2 branch زي :
- main branch: commit1, c2 وبعد كده جيت عملت منه development branch
- development branch: c1, c2 وزودت عليه c3, c4 وبعد كده انتقلت ل main branch وعملت شغل هناك وطبعا عملتلهم commits ودا معناه اني عدلت فيه
- main branch: c1, c2, c3, c4, --> c3, c4 دول مش نفس اللي في ال development 3,4 دول مجرد ترقيم فقط لكن ال commits نفسهم مختلفين
