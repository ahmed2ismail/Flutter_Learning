_______________________________ Git Tags ____________________________________

- الـ Tags بنستخدمها عشان نعلم نقاط معينة في تاريخ المشروع كإصدارات (Versions) زي `v1.0`, `v2.0`.
- **أنواع الـ Tags:**
1- **Lightweight**: مجرد مؤشر لـ commit معين (زي الـ branch بس مش بيتحرك).
   - `git tag v1.0`
2- **Annotated**: مخزن ككائن كامل في قاعدة بيانات Git، فيه اسم اللي عمل الـ tag، إيميله، التاريخ، ورسالة (وده الأفضل).
   - `git tag -a v1.1 -m "Release version 1.1"`

- **أوامر التعامل مع الـ Tags:**
- `git tag`: عرض كل الـ tags.
- `git show v1.1`: عرض تفاصيل الـ tag والـ commit المرتبط بيه.
- `git push origin v1.1`: لرفع tag معين للـ Remote.
- `git push origin --tags`: لرفع كل الـ tags اللي عملتها مرة واحدة.
- `git tag -d v1.1`: لحذف tag من الـ Local.
- `git push origin --delete v1.1`: لحذف tag من الـ Remote.

