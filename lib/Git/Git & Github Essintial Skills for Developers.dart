- دايما بيتذكروا مع بعض وهما حاجتين منفصلتين ولكن مرتبطين ببعض جدا
- دلقوت فيه two accounts :
1- accountA (Owner) عنده ال remote repo اللي غيره وهو بيشتغلوا عليها
2- accountB (Collaborator) عمل clone repo ثم اصبحت علي جهازه local repo وطبعا عمل تعديلات وبعدين عمل push فالتعديلات دي راحت علي ال remote repo بتاعت accountA
وبعدين جه accountC (واحد غريب) عايز يعمل contribute لل repo بتاعتي لانه عمل اضافات وعايز يبعتها لل remote repo وبما ان انا مدتلهوش صلاحيات لدا فطريقة ال collaboration مش نافعة معاه
ومن هنا يظهر اهمية ال Fork & Pull Request : دي الطريقة المتبعة لعمل ال contribute وطبعا لازم ال remote repo تكون public لانها لو private فهو مش هيقدر يشوفها اساسا

---- خطوات ال Fork & Pull Request >>> نظري >>>
1- accountC بيعمل Fork يعني بياخد نسخة من ال remote repo وافتكر ان دي نسخة وليست الاصلية وبتتعمل علي github accountC يعني علي الاكونت بتاعه وبكدا هو بق عنده remote repo (Copy)
2- بيعمل clone ل remote repo (Copy) علي جهازه وبكده بق عنده local repo وبعدين هيعمل تعديلات ويعملها push فهتروح علي ال remote repo (Copy)
