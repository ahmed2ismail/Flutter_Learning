- للتذكير ال github remote repo دي ممكن يكون شغال معايا تيم عليها فلما اجي اشتغل علي المشروع لازم اتاكد اني ماشي مع ال remote repo بالظبط عشان لو زميلي رفع اضافات ليها فهبقي انا كده شغال علي نسخة قديمة فلازم اجيب الاضافات دي لمشروعي وبعدين اكمل شغل
- فدايما كل لما اجي افتح المشروع وابدا شغل اعمل pull عشان احدث مشروعي فهيجيبلي التغييرات بتاعة البرانشز علطول لمشروعي ويتحدث
- امر git pull لو عاملين -u(-set--upstream) ولو مش عاملينها يبق git pull origin branchName وعملية ال pull دي زي ال merge بالظبط يعني هو فعليا بيعمل merge from remote branchName on local branchName
- git pull(يتكون من) = git fetch(عشان يجيب كل المعلومات) + git merge(لاظهار التغييرات عندي)

