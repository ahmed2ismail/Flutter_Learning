- git branch -r : اعرضلي كل ال remote branches فقط
- git branch -a (all يعني) : امر عرض كل ال branches اللي انا عملتها في المشروع دا including remote branches اللي اتعملت لل remote repository بتاعتي
لو انا نفذت git branch -a هيجيبلي :
* development --> دا اللي انا واقف عليه عشان متعلم ب * ودا البرانش الحديث بتاعي يعني احدث شغل فيه
main --> احدث برانش وشايل احدث شغل يعني هو احدث نسخة
remotes/origin/development --> ظهر عشان عملت push لتعديلاتي اللي في development branch --> دا copy يعني النسخة القديمة اللي شايلة الشغل اللي كان موجود قبل الاحدث مباشرة
remotes/origin/main --> ظهر عشان عملت push لتعديلات اللي في main branch --> copy نسخة قديمة من ال remote main branch
- باختصار لما تيجي تعمل push انا هعمل نسخة من ال remote branch عشان كده بيكون اسمه remote/ و origin/ عشان عنوان ال repo و RemoteBranchName وبتتعمل عشان ممكن في المستقبل اقارن مبين النسخة الحديثة بتاعتي اللي انا شغال عليها اللي هي local branch branchName وبين النسخة القديمة الموجودة بالفعل اللي هي ال copy اللي اتعملت دي
- ال remotes/origin/RemoteBranchName --> بتسمي remote Tracking Branches وهي ال Branches اللي بنشأها لما بعمل push وبتابع (track) بيها التغيرات اللي بتحصل في ال remote branch
- كمثال لو انا عملت تعديلات في local development branch ف development branch هيتغير اما ال remotes/origin/development مش هيتغير لانه بيتابع ال remote development branch فقط يعني ملوش علاقة بال local اما لو عملت push بق فهو كمان هيتعمله updated لانه بي track ال remote
- وطبعا اقدر اقارن بق عادي يعني :
* development --> دا البرانش بتاعك
remotes/origin/development --> ودا البرانش اللي كلنا شغالين عليه
فكده في اي وقت دلقوت اقدر اقارن الاتنين ببعض لان ال remote tracking branch ماشي مع ال remote branch والاتنين زي بعض بالظبط
====== ال remote branch ممكن يكون احدث او اقدم من ال local branch بتاعي ??
Updated ---> احدث في حال لو انا عملت تعديلات في ال local branch ومعملتش push فبالتالي ال remote branch و remote tracking branch متحدثوش اما لو عملت push فالاتنين هيتحدثوا
Oldest ---> اقدم لو انا عملت اضافات او تعديلات من ال repo من github ومعملتش fetch للاضافات دي بس ولو انا جيت اعمل git checkout origin/RemoteBranchName عشان اجيب التحديثات اللي اتعملت فيه لل local branch بتاعي هلاقي انه مجابليش اي حاجة ودا بسبب نقطة مهمة
وهي ان ال remote tracking branch بيتتبع ال remote branch ولكن مش بيتغير بشكل تلقائي فلو انا عملت تغيير في ال remote branch بتاعي فانا لازم اغير ال remote tracking branch بنفسي ودا هيكون عن طريق امر git fetch origin
- git fetch او git fetch origin --> بيجيب كل المعلومات الموجودة في ال remote repo بتاعتي زي البرانشز الجديدة اللي اتعملت وغيرها وبيعمل update لكل ال remote tracking branches وال local branches بتوعي متغيروش
فدلوقت لو جيت اروح بق علي البرانش ال remote اللي علي جهازي اللي هو النسخة اللي اسمه مثلا origin/development وعملت git checkout remotes/origin/development فهلاقيه جابلي الحجات اللي اتضافت لل remote development branch وغير كدا ال local development branch بتاعي متغيرش خالص لاني مجيتش جمبه وبكدا هو بقي اقدم من ال remote branch و ال remote traching branch
ملحوظة : git fetch بتجيبلي ال information بتاعة ال remote repo بشكل عام ولكن مش بتجيب حاجة للبرانش فحتي لو البرانش فيه تغيير وعملت الامر دا مفيش حاجة هتتغير في البرانش بتاع مشروعي لان لازم اعمل git checkout remotes/origin/RemoteBranchName وعشان اتفادي الصداع دا هستخدم عطلول امر ال pull

