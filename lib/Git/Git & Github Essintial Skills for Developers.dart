--------------------------------- Teams -------------------------------
_______________________________________________________________________

- الـ **Teams** هي الميزة الأهم والأقوى في الـ Organization، وهي اللي بتفرقها عن الحساب الشخصي (Personal Account).
- فكرتها ببساطة: بدل ما تضيف كل مبرمج لوحده على كل مشروع (Repository) وتحدد صلاحياته، إنت بتعمل "مجموعة" أو "فريق" وبتضيف فيه الأعضاء، وبعدين بتدي الفريق ده صلاحية معينة على ريبوزيتوري معينة.

### 1- ليه بنستخدم الـ Teams؟
- **الإدارة الجماعية:** لو عندك 50 مبرمج، مش منطقي تدخل على كل ريبو وتضيف الـ 50 واحد. إنت بتعمل فريق اسمه "Developers" وتضيفهم فيه مرة واحدة.
- **توزيع المهام:** تقدر تقسم المنظمة لفرق تخصصية (مثلاً: `Frontend-Team`, `Backend-Team`, `QA-Team`).
- **السرعة في التوظيف:** لما مبرمج جديد ينضم للشركة، بمجرد ما تضيفه لفريق الـ "Mobile-Devs"، بياخد تلقائياً صلاحية الوصول لكل المشاريع اللي الفريق ده شغال عليها.

### 2- أنواع الـ Teams (Visibility):
- **Visible:** أي حد في المنظمة يقدر يشوف الفريق ده ويبعتله Mention (زي `@org/frontend-team`).
- **Secret:** مفيش حد يقدر يشوف الفريق ده ولا الأعضاء اللي فيه غير الـ Owners والناس اللي جوه الفريق نفسه.

### 3- مستويات الهيكلة (Parent & Child Teams):
- الـ GitHub بيسمحلك تعمل **Nested Teams** (فرق متداخلة):
  - مثلاً: فريق كبير اسمه `Engineering`.
  - جواه فريقين أصغر: `Web-Dev` و `Mobile-Dev`.
  - **القاعدة:** أي صلاحية بياخدها الـ Parent Team (الكبير)، بتتورث تلقائياً للـ Child Teams (الصغيرة). ده بيسهل إدارة الصلاحيات العالية للمديرين.

### 4- كيفية إنشاء فريق (Steps):
1. ادخل على صفحة الـ **Organization** بتاعتك.
2. اضغط على تبويب **Teams**.
3. اضغط على زرار **New team**.
4. اكتب اسم الفريق (مثلاً `Backend-Developers`) ووصف بسيط ليه.
5. حدد الـ **Visibility** (سواء `Visible` أو `Secret`).
6. اضغط **Create team**.
7. بعد ما الفريق يتنشئ، تقدر تضيف فيه الـ **Members** من خانة "Add a member".
8. عشان تربط الفريق بمشروع معين، ادخل على تبويب **Repositories** جوه صفحة الفريق واضغط **Add repository** وحدد الصلاحية (Read, Write, Admin, etc.).

-------------------------- Team Discussions ---------------------------

- فيه ميزة مهمة جداً جوه الـ Teams وهي الـ **Team Discussions**:
- **إيه هي؟**: هي مساحة مخصصة للمناقشات بين أعضاء الفريق الواحد بعيداً عن الـ Issues العامة بتاعة المشروع.
- **ليه بنستخدمها؟**: 
  1- لو عايزين نناقش فكرة تقنية (Technical Design) قبل ما نبدأ كود.
  2- لو فيه إعلانات داخلية للفريق (Internal Announcements) زي مواعيد الاجتماعات.
  3- لتبادل الروابط والمصادر التعليمية الخاصة بالتخصص بتاع الفريق.
- **الخصوصية**: المناقشات دي بتكون مرئية فقط لأعضاء الـ Organization (لو الفريق Visible) أو لأعضاء الفريق بس (لو الفريق Secret).

------------------------ Code Review Assignments ----------------------

- الـ **Code Review Assignment** هي خاصية ذكية في الـ Teams بتوزع الشغل تلقائياً:
- لما حد يبعت Pull Request (PR) ويختار الفريق كـ Reviewer، الـ GitHub بيقدر يوزع الطلب ده على أعضاء الفريق بشكل آلي بطريقتين:
  1- **Round Robin**: بيوزع الطلبات بالترتيب على المبرمجين عشان يضمن إن مفيش حد شايل شغل أكتر من التاني.
  2- **Load Balance**: بيشوف مين أقل واحد عنده PRs مفتوحة وبيبعتله الطلب الجديد.
- **إزاي بنفعلها؟**: من إعدادات الفريق (Team Settings) > Code review assignment.

--------------------------- Team Profile Page -------------------------

- كل فريق ليه صفحة بروفايل خاصة بيه بتعرض:
  1- **Members**: مين الناس اللي في الفريق ده.
  2- **Child Teams**: الفرق الفرعية التابعة ليه.
  3- **Repositories**: إيه المشاريع اللي الفريق ده ليه صلاحية عليها.
  4- **Activity**: ملخص لآخر التعديلات والـ Commits اللي عملها أعضاء الفريق.

-------------------------- Team Maintainers ---------------------------
- الـ **Team Maintainers** هما الأشخاص اللي ليهم صلاحية إدارة الفريق نفسه (مش بالضرورة يكونوا Owners للمنظمة):
- **صلاحياتهم**:
  1- إضافة أو حذف أعضاء من الفريق (Add/Remove Members).
  2- تغيير إعدادات الفريق (اسم الفريق، الوصف، الـ Visibility).
  3- إنشاء فرق فرعية (Child Teams) تحت الفريق بتاعهم.
  4- قبول أو رفض طلبات الانضمام للفريق.
- **ليه بنستخدمهم؟**: عشان الـ Owner بتاع المنظمة ميتعبش في إدارة كل الفرق الصغيرة، فبيعين "رئيس فريق" (Maintainer) لكل مجموعة وهو اللي يدير فريقه بنفسه.

