==== ثالثا في حالة ان انا عدلت في ملف معموله track وموجود في ال staging area واتعمله commit ولقيت مثلا ان التعديلات دي ملهاش لازمة فعايز اتراجع عنها >>>>>>>
- لما بعمل commit فانا بلجا ل git reset عشان اتخلص من ال commit دا وهي بتتخلص من اخر commit بس
- git reset head~number of commits i want to delete : ال head بتشاور علي اخر commit فانا هنا بقول انا عايز اعمل reset بداية من ال commit دا وبعد العلامة دي "~" بكتب عدد ال commit اللي انا عايز الغيها من ال commit دا
- مثال : git reset head~1 : يعني كدا هيلغي اخر commit بس
- مثال : git reset head~2 : يعني كدا هيلغي اخر 2 commit بس
- مثال : git reset head~3 : يعني كدا هيلغي اخر 3 commit بس وهكذا
- والاوامر دي بتلغي ال commit وبتشيل الملف من ال staging area يعني بتلغي التعديلات بس
- git reset --soft head~1 : ال --soft خلته يرجع خطوة واحدة لوره يعني لغت ال commit بس التعديلات فضلت زي مهي يعني الملف فضل في ال staging area
- git reset --hard head~1 : --hard بتخليني اتخلص من التعديلات بالكامل يعني لغت ال commit وكمان طلعته من ال staging area وكمان مسحت الملف دا من ال working directory

- git reset --mixed head~1 : (الوضع الافتراضي Default) بيقوم بإلغاء الـ commit وبيرجع الملفات لحالة الـ Unstaged، يعني التعديلات بتفضل موجودة في الـ Working Directory بس بتخرج من الـ Staging Area (بتحتاج تعمل git add من جديد).
- git reset --soft head~1 : بيقوم بإلغاء الـ commit فقط، وبتقدر تلاقي التعديلات لسه موجودة وجاهزة في الـ Staging Area (يعني كأنك عملت git add وخلاص واقف على خطوة الـ commit).
- git reset --hard head~1 : (الأمر الأخطر) بيقوم بمسح الـ commit وكل التعديلات اللي كانت فيه نهائياً من الـ Staging Area ومن الـ Working Directory، وبيرجع المشروع لحالته بالظبط عند الـ commit اللي قبله.
- git reset --merge : بيستخدم غالباً لإلغاء عملية Merge حصل فيها Conflict، وبيرجع الملفات لحالتها قبل ما تبدأ عملية الـ Merge مع الحفاظ على التعديلات اللي مكنتش جزء من الـ Merge.
- git reset --keep : بيشبه الـ --hard بس أمن شوية، لأنه لو فيه تعديلات في الـ Working Directory لسه معملتلهاش commit وهتتمسح بسبب الـ reset، الأمر ده هيرفض يتنفذ عشان يحمي شغلك من الضياع.
- git reset <commit_id> : بيخلي الـ Head يشاور على commit معين قديم وبيلغي كل اللي بعده بناءً على الـ mode اللي اخترته (soft, hard, mixed).
- git reset <commit_id> -- <file_path> : بيستخدم عشان يرجع ملف معين لحالته في commit قديم أو يشيله من الـ Staging Area من غير ما يأثر على باقي الملفات أو الـ commits التانية.
- git reset --patch : بيسمحلك تختار أجزاء معينة (Hunks) من الملفات عشان تشيلها من الـ Staging Area بدل ما تشيل الملف كله.

